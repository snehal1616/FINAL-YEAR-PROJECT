window.i18n = {
    currentLang: localStorage.getItem('preferredLang') || 'en',
    
    setLang: function(lang) {
        this.currentLang = lang;
        localStorage.setItem('preferredLang', lang);
        this.applyTranslations();
        
        // Update user profile preference on server
        fetch('/api/profile', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ lang: lang })
        }).catch(e => console.error("Failed to sync language preference"));
    },
    
    t: function(key, params = {}) {
        const langData = translations[this.currentLang] || translations['en'];
        let text = langData[key] || translations['en'][key] || key;
        
        for (const [pKey, pVal] of Object.entries(params)) {
            text = text.replace(`{${pKey}}`, pVal);
        }
        return text;
    },
    
    applyTranslations: function() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (el.tagName === 'INPUT' && el.type === 'text') {
                el.placeholder = this.t(key);
            } else if (el.tagName === 'BUTTON' && el.title) {
                el.title = this.t(key);
            } else {
                el.innerText = this.t(key);
            }
        });
        
        // Update document title if needed
        const titleKey = document.body.getAttribute('data-i18n-title');
        if (titleKey) document.title = this.t(titleKey) + " | Cureastic AI";

        // Update theme button icon/text separately as it's dynamic
        const themeBtn = document.getElementById('themeToggleBtn');
        if (themeBtn) {
            const isDark = document.documentElement.getAttribute('data-theme') === 'dark';
            const icon = isDark ? 'fa-sun' : 'fa-moon';
            const text = isDark ? this.t('theme_light') : this.t('theme_dark');
            themeBtn.innerHTML = `<i class="fa-solid ${icon}"></i> ${text}`;
        }
    }
};

document.addEventListener('DOMContentLoaded', () => { window.i18n.applyTranslations(); });
